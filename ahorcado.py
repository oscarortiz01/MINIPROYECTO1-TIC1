def jugar():
    """Funci√≥n principal que contiene toda la l√≥gica de una partida del juego del ahorcado."""
    # Fase de configuraci√≥n de la partida.
    while True:
        letras = leeyvalida(3, 10, "¬øCon cu√°ntas letras quieres jugar? (3-10): ")
        # Filtra la lista de Pok√©mon para quedarse solo con los que tienen la longitud deseada.
        posibles = [p for p in pokemones if len(p) == letras]
        if posibles:
            break # Si se encontraron Pok√©mon, sale del bucle.
        else:
            print("‚ö†Ô∏è No hay Pok√©mon con esa cantidad de letras. Intenta con otro n√∫mero.")

    # Elige un Pok√©mon al azar de la lista filtrada y lo convierte a min√∫sculas.
    palabra = random.choice(posibles).lower()
    oculta = ["_"] * len(palabra) # Crea la palabra oculta con guiones.

    # Pide el n√∫mero de vidas.
    max_vidas = leeyvalida(1, 20, "Ingrese el n√∫mero de vidas (1-20): ")
    vidas = max_vidas

    # Inicializa los LEDs con el estado actual de las vidas.
    actualizar_leds_estado(vidas, max_vidas)

    print(f"¬°A jugar! El Pok√©mon tiene {len(palabra)} letras.")
    print(" ".join(oculta)) # Muestra la palabra oculta inicial (ej: _ _ _ _).

    usadas = set() # Un conjunto para guardar las letras ya usadas y evitar repeticiones.

    # Bucle principal del juego: contin√∫a mientras queden vidas Y queden letras por adivinar.
    while vidas > 0 and "_" in oculta:
        letra = elegir_letra_con_joystick(usadas)
        usadas.add(letra) # A√±ade la nueva letra al conjunto de usadas.

        # Comprueba si la letra est√° en la palabra.
        if letra in palabra:
            # Si est√°, reemplaza los guiones bajos correspondientes.
            for i, c in enumerate(palabra):
                if c == letra:
                    oculta[i] = c
            print("¬°Correcto!")
        else:
            # Si no est√°, resta una vida.
            vidas -= 1
            print(f"Letra incorrecta. Te quedan {vidas} vidas.")
            sonido_error_vida() # Reproduce el sonido de error.
            actualizar_leds_estado(vidas, max_vidas) # Actualiza los LEDs.

        # Muestra el estado actual de la palabra y las letras usadas.
        print(" ".join(oculta))
        print("Letras usadas:", ", ".join(sorted(usadas)))

    # Al salir del bucle, comprueba la condici√≥n de victoria o derrota.
    if "_" not in oculta:
        print(f"üéâ ¬°Ganaste! El Pok√©mon era: {palabra.capitalize()}")
        sonido_victoria()
    else:
        print(f"‚ùå Te quedaste sin vidas. El Pok√©mon era: {palabra.capitalize()}")
        sonido_derrota()

    time.sleep(3) # Pausa de 3 segundos antes de continuar.

# --- BUCLE PRINCIPAL DE EJECUCI√ìN ---

# Crea un "evento" que servir√° como se√±al para detener el hilo de parpadeo de forma limpia.
stop_event = threading.Event()
# Crea el hilo (thread) que ejecutar√° la funci√≥n `blink_thread_func`.
# Se le pasan los argumentos necesarios: el evento de parada, el diccionario de vidas y el candado.
# daemon=True significa que el hilo se detendr√° autom√°ticamente si el programa principal termina.
blink_thread = threading.Thread(target=blink_thread_func, args=(stop_event, vidas_info, gpio_lock), daemon=True)
# Inicia la ejecuci√≥n del hilo. A partir de aqu√≠, `blink_thread_func` corre en paralelo al c√≥digo principal.
blink_thread.start()

try:
    # Bucle que permite jugar m√∫ltiples partidas.
    while True:
        jugar() # Llama a la funci√≥n principal del juego.
        seguir = input("¬øQuieres seguir jugando? (s/n): ").lower()
        if seguir != "s":
            print("Gracias por jugar, hasta la pr√≥xima!")
            break
except KeyboardInterrupt:
    # Esto se ejecuta si el usuario presiona Ctrl+C para salir del programa.
    print("\nInterrumpido por usuario.")
finally:
    # Este bloque se ejecuta SIEMPRE al final, ya sea que el programa termine
    # normalmente o por una interrupci√≥n. Es ideal para la limpieza de recursos.
    print("Limpiando y saliendo.")
    stop_event.set() # Env√≠a la se√±al al hilo de parpadeo para que termine su bucle.
    led1.off()       # Apaga ambos LEDs.
    led2.off()
    buzzer.stop()    # Se asegura de que el zumbador no se quede sonando.